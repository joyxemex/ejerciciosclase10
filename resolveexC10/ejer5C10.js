// 1. DECLARACIÓN Y ASIGNACIÓN
let primerNumero = 20;
let segundoNumero = 5;

// 2. OPERACIONES MATEMÁTICAS
// Guardamos los resultados en nuevas variables.
let resultadoSuma = primerNumero + segundoNumero;
let resultadoResta = primerNumero - segundoNumero;
let resultadoMultiplicacion = primerNumero * segundoNumero;
let resultadoDivision = primerNumero / segundoNumero;

// 3. MOSTRAR RESULTADOS
console.log("--- Resultados Matemáticos ---");
console.log("Suma: " + resultadoSuma);            // Debería dar 25
console.log("Resta: " + resultadoResta);          // Debería dar 15
console.log("Multiplicación: " + resultadoMultiplicacion); // Debería dar 100
console.log("División: " + resultadoDivision);    // Debería dar 4